    <span class="font-bold text-darkblue">Key Responsibilities:</span><br><br>

    Approve major decisions related to the company’s direction and financial management.<br>
    Ensure legal compliance and ethical standards across all levels of the business.<br>
    Oversee the company’s sustainability initiatives and corporate social responsibility efforts.<br>
    Assess and monitor business risks and opportunities.<br>
    Support and evaluate the performance of the CEO and senior leadership team.<br><br>

    <span class="font-bold text-2xl">Board Meetings</span><br><br>
    
